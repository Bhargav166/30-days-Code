# Check the data type of all your variables using the type() built-in function.

first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")
country = input("Enter your country name: ")
city = input("Enter your city name: ")
age = int(input("Enter your age: "))
dob = input("Enter your DOB: ")


print('Data type of FirstName is {}'.format(type(first_name)))
print('Data type of LastName is {}' .format(type(last_name)))
print('Data type of Contry is {}'.format(type(country)))
print('Data type of City {}' .format(type(city)))
print('Data type of Age {}' .format(type(age)))
print('Data type of DOB {}' .format(type(dob)))
